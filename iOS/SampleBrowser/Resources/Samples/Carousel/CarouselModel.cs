        public CarouselViewModel()
        {
            GetColor();
            GetIcon();
            ObservableCollection<string> applicationicons = GetApplicationIcon();
            ObservableCollection<string> officeicons = GetOfficeIcon();
            ObservableCollection<string> transporticons = GetTransportCollection();
            var assembly = typeof(CarouselViewModel).GetTypeInfo().Assembly;
        
            Stream stream = assembly.GetManifestResourceStream("SampleBrowser.Resources.Images.Carousel.CustomSearch.txt");


            if(stream!=null)
            {
                string text = "";
                using (var reader = new System.IO.StreamReader(stream))
                {
                    while ((text = reader.ReadLine()) != null)
                    {
                        string[] splits = text.Split('*');

                        if (splits.Length == 3)
                        {

                            try
                            {
                                if (applicationicons.Contains(splits[0]))
                                {
                                    CarouselModel model = new CarouselModel();
                                    model.ItemColor = PickColor();
                                    model.Name = splits[0];
                                    model.Unicode = s[splits[1]];
                                    ApplicationCollection.Add(model);
                                    DataCollection.Add(model);
                                    TempCollection.Add(model);
                                }
                                else if (officeicons.Contains(splits[0]))
                                {
                                    CarouselModel model = new CarouselModel();
                                    model.ItemColor = PickColor();
                                    model.Name = splits[0];
                                    model.Unicode = s[splits[1]];
                                    OfficeCollection.Add(model);
                                    DataCollection.Add(model);
                                    TempCollection.Add(model);
                                }
                                else if (transporticons.Contains(splits[0]))
                                {
                                    CarouselModel model = new CarouselModel();
                                    model.ItemColor = PickColor();
                                    model.Name = splits[0];
                                    model.Unicode = s[splits[1]];
                                    TransportCollection.Add(model);
                                    DataCollection.Add(model);
                                    TempCollection.Add(model);
                                }
                                else
                                {
                                    CarouselModel model = new CarouselModel();
                                    model.Name = splits[0];
                                    model.Unicode = s[splits[1]];
                                    DataCollection.Add(model);
                                    TempCollection.Add(model);
                                }

                            }
                            catch (Exception e)
                            {
                                var ee = e.Message;
                            }

                        }
                    }
                }
            }
          
            for (int j = 0; j < 2; j++)
            {
                for (int i = 0; i < this.TempCollection.Count; i++)
                {
                    DataCollection.Add(this.TempCollection[i]);
                }
            }


